-- termcast 用テーブル（間借り先と衝突しないよう termcast_ プレフィックス）
create table if not exists termcast_terms (
  id uuid primary key default gen_random_uuid(),
  term text not null unique,
  reading text not null default '',
  category text not null default '',
  difficulty int not null default 1 check (difficulty between 1 and 3),
  status text not null default 'pending' check (status in ('pending','generated','published')),
  published_at timestamptz,
  youtube_video_id text,
  tiktok_draft_id text,
  drive_link text,
  created_at timestamptz not null default now()
);
create index if not exists termcast_terms_pick_idx on termcast_terms (status, difficulty);

